#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    cout << pow(2, 5) << endl;
    cout << sqrt(49) << endl;
    cout << round(4.6) << endl;
    cout << ceil(3.3) << endl; //ceil for rounding up num
    cout << floor(2.9) << endl; //floor for rounding down num
    cout << fmax(2, 10) << endl; //fmax for finding higher num
    cout << fmin(9, 4) << endl; //fmin for finding lower num

    return 0;
}
